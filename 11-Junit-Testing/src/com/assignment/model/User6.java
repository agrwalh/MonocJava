package com.assignment.model;

public class User6 {
	private String name;
	private int age;
	private String status;

	public User6(String name, int age, String status) {
        this.name = name;
        this.age = age;
        this.status = status;
    }

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getStatus() {
		return status;
	}
}
